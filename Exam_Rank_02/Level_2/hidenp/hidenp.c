#include <unistd.h>

int	main (int ac, char **av)
{
	int	idx = 0;
	int	c = 0;
	if (ac == 3)
	{
		while ((av[2][c] != '\0') && (av[1][idx] != '\0'))
		{
			if (av[2][c] == av[1][idx])
				idx++;
			c++;
		}
		if (av[1][idx] == '\0')
			write(1, "1", 1);
		else
			write(1, "0", 1);
	}
	write(1, "\n", 1);
	return (0);
}