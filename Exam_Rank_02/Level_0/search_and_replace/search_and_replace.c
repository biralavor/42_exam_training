#include <unistd.h>

int main(int ac, char **av)
{
	int idx = 0;
	if (ac == 4 && av[2][1] == '\0' && av[3][1] == '\0')
	{
		while (av[1][idx] != '\0')
		{
			if (av[1][idx] == *av[2])
				av[1][idx] = *av[3];
			write(1, &av[1][idx], 1);
			idx++;
		}
	}
	write(1, "\n", 1);
	return(0);
}
