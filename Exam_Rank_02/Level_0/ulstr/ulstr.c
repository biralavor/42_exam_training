#include <unistd.h>

int	main (int ac, char **av)
{
	int	idx = 0;

	if (ac == 2)
	{
		while (av[1][idx] != '\0')
		{
			if (av[1][idx] >= 65 && av[1][idx] <= 90)
				av[1][idx] += 32;
			else if (av[1][idx] >= 97 && av[1][idx] <= 122)
				av[1][idx] -= 32;
			write(1, &av[1][idx], 1);
			idx++;
		}
	}
	write(1, "\n", 1);
	return(0);
}
